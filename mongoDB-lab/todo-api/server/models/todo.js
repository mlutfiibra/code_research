var mongoose = require('mongoose');

// specifies the todo you want to add
var Todo = mongoose.model('Todo', {
	text: {
		type: String,
		required: true,
		minlength: 1,
		trim: true
	},
	completed: {
		type: Boolean,
		default: false
	},
	completedAt: {
		type: Number,
		default: null
	}
});

//exports to use (require) by other files
module.exports = {Todo};